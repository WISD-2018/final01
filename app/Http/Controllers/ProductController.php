     */
    public function store(Request $request)
    { 
        $this->validate($request, [
            'name' => 'required|min:4',
            'price'=> 'required|min:1',
        ]);
        
        $products = Product::create(['name' => $request->name,'price' => $request->price,'image' => $request->image,]);
